package main

import "fmt"

func main() {
	// ==========================================
	// JKad03D「エスケープシーケンス」（入門編 P.32）
	// リスト1を入力し動作確認せよ。さらに以下の指示に従って課題完成時の画面（After）のように表示されるようにプログラムを作成せよ。
	// 「こんにちは！」と「今日もよい天気ですね！」との間に改行を入れる。
	// ただし println を 2 つに分けてはいけない。
	// 「Java 言語」をダブルクォーテーション（"）で囲む。
	// ==========================================
	fmt.Println("--- JKad03D ---")
	fmt.Println("こんにちは！\n今日もよい天気ですね！")
	fmt.Println("\"Java言語\"を勉強しています！")

	// ==========================================
	// JKad03C「文字列と数値の連結」（入門編 P.39）
	// 課題完成時の画面のように表示されるようにリスト1のプログラムを完成させよ。
	// int a = 5;
	// int b = 10;
	// 変数aの値を表示する処理
	// 変数bの値を表示する処理
	// a+bの結果を表示する処理
	// ==========================================
	fmt.Println("\n--- JKad03C ---")
	a := 5
	b := 10
	fmt.Println("変数aの値は", a, "です")
	fmt.Println("変数bの値は", b, "です")
	fmt.Println("a + b =", a+b)

	// ==========================================
	// JKad03B1「算術演算の短縮表現」（入門編 P.45、JKad02B.txt のコードをコピーして作成）
	// 四則演算と余りの演算を短縮表現に変更せよ。
	// 変数xの値を表示します！ (5)
	// 変数xに14を足します！ (19)
	// 変数xから3を引きます！ (16)
	// 変数xに7をかけます！ (112)
	// 変数xを10で割ります！ (11)
	// 変数xを4で割った余りを求めます！ (3)
	// ==========================================
	fmt.Println("\n--- JKad03B1 ---")
	x := 5
	fmt.Println("変数xの値を表示します！", x)
	x += 14
	fmt.Println("変数xに14を足します！", x)
	x -= 3
	fmt.Println("変数xから3を引きます！", x)
	x *= 7
	fmt.Println("変数xに7をかけます！", x)
	x /= 10
	fmt.Println("変数xを10で割ります！", x)
	x %= 4
	fmt.Println("変数xを4で割った余りを求めます！", x)

	// ==========================================
	// JKad03B2「インクリメントとデクリメント」（入門編 P.48「前置と後置」）
	// リスト1のコードを入力・実行してインクリメントとデクリメントの動作確認をせよ。
	// ※注意: Go言語ではインクリメント/デクリメント（++ / --）は文であり、式の中（println等の中）では使えません。前置（++i）も存在しません。Go言語の仕様に合わせて動作確認を行ってください。
	// ==========================================
	fmt.Println("\n--- JKad03B2 ---")
	i := 10
	fmt.Println("i =", i)
	i++
	fmt.Println("i++ →", i)
	i++
	fmt.Println("i++ →", i)
	i--
	fmt.Println("i-- →", i)

	// ==========================================
	// JKad03A「九九を表示しよう！」
	// 以下の手順で、九九を表示する処理を作成せよ。
	// 手順① 変数iで指定された段を表示する処理を作成する。
	// 手順② 「一の段」から「九の段」まで表示するように修正する。
	// 数値の前にタブを入れるときれいに整列して表示される。
	// ==========================================
	fmt.Println("\n--- JKad03A ---")
	for i := 1; i <= 9; i++ {
		for j := 1; j <= 9; j++ {
			fmt.Printf("\t%d", i*j)
		}
		fmt.Println()
	}

	// ==========================================
	// JKad03S「ようこそ！ECC コーヒーへ」※こちらは課題 S です。
	// あこがれの「ECC コーヒー」で食事をした！食事内容は以下の通り。支払金額を計算する処理を作成せよ。
	// なお、可能な限り算術演算の短縮表現を使うこと。
	// 食事内容（値段は税抜き、これに10％の消費税が加算される）
	// ・コーヒー 290 円
	// ・ドーナツ 250 円
	// ・チキンサラダ 380 円
	// ・チーズケーキ 420 円
	// 各メニューの金額と小計を表示。最後に消費税（10%）を計算して加算する。
	// ==========================================
	fmt.Println("\n--- JKad03S ---")
	coffee := 290
	donut := 250
	chickenSalad := 380
	cheeseCake := 420

	fmt.Println("コーヒー:", coffee, "円")
	fmt.Println("ドーナツ:", donut, "円")
	fmt.Println("チキンサラダ:", chickenSalad, "円")
	fmt.Println("チーズケーキ:", cheeseCake, "円")

	subtotal := coffee + donut + chickenSalad + cheeseCake
	fmt.Println("小計:", subtotal, "円")

	subtotal += subtotal / 10
	fmt.Println("消費税込み合計:", subtotal, "円")
}
